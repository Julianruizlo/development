    public void cargarCb() {
        ArrayList<Especie> especies = Controlador.getEspecies();

        for (Especie especie : especies) {
            CbEspecie.addItem(especie.getNombre());
        }

        ArrayList<Sector> sectores = Controlador.getSectores();

        for (Sector sector : sectores) {
            CbSector.addItem(String.valueOf(sector.getNumero()));
        }
        ArrayList<Pais> paises = Controlador.getPaises();
        for (Pais pais : paises) {
            CbPaisOrigen.addItem(pais.getNombre());
        }

        for (TipoAlimentacion tipo : Controlador.getTiposAlimentacion()) {
            CbTipoAlimentacion.addItem(tipo);
        }

    }

    public void anadirListeners() {

        BtnVolver.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                dispose(); // Cierra la ventana actual
                MenuView view = new MenuView();
                view.setLocationRelativeTo(null);
                view.setVisible(true);
            }
        });

        CbTipoAlimentacion.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                habilitarValorfijo();
            }
        });

        BtnGuardar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (CbTipoAlimentacion.getSelectedItem() == TipoAlimentacion.HERBIVORO) {

                    try {
                        Controlador.guardarAnimal(new Herbivoro(
                                getEdad(), getPeso(), getEspecie(), getSector(), getValorFijo(), getPais()
                        ));

                    } catch (InvalidPropertiesFormatException ev) {
                        // Manejo del error (mostrar mensaje, log, etc.)
                        ev.printStackTrace();

                    }

                } else if (CbTipoAlimentacion.getSelectedItem() == TipoAlimentacion.CARNIVORO) {

                    try {
                        Controlador.guardarAnimal(new Carnivoro(
                                getEdad(), getPeso(), getEspecie(), getSector(), getPais()
                        ));

                    } catch (InvalidPropertiesFormatException ev) {
                        // Manejo del error (mostrar mensaje, log, etc.)
                        ev.printStackTrace();

                    }

                }
            }
        });
    }

    public Especie getEspecie() {
        String nombreEspecie = String.valueOf(CbEspecie.getSelectedItem());
        for (Especie especie : Controlador.getEspecies()) {
            if (nombreEspecie.equals(especie.getNombre())) {
                return especie;
            }
        }
        return null;
    }

    public Pais getPais() {
        String nombrePais = String.valueOf(CbPaisOrigen.getSelectedItem());
        for (Pais pais : Controlador.getPaises()) {
            if (nombrePais.equals(pais.getNombre())) {
                return pais;
            }
        }
        return null;
    }

    public Sector getSector() {
        int numeroSector = Integer.parseInt(CbSector.getSelectedItem().toString());

        for (Sector sector : Controlador.getSectores()) {
            if (numeroSector == sector.getNumero()) {
                return sector;
            }
        }
        return null;
    }

    public TipoAlimentacion getAlimentacion() {
        return (TipoAlimentacion) CbTipoAlimentacion.getSelectedItem();
    }

    public double getValorFijo() {
        return Double.parseDouble(TfValorFijo.getText());
    }

    public int getEdad() {
        return Integer.parseInt(TfEdad.getText());
    }

    public double getPeso() {
        return Double.parseDouble(TfPeso.getText());
    }

    public void habilitarValorfijo() {
        if (CbTipoAlimentacion.getSelectedItem() == TipoAlimentacion.HERBIVORO) {
            TfValorFijo.setEnabled(true);
        } else {
            TfValorFijo.setEnabled(false);
        }
    }

